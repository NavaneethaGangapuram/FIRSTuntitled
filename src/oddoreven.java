public class oddoreven {
public static void main(String[] args)
{
    int a=13;
    // since we need to find out whether the number is odd or even "%" indicates the remainder values so when it is equal to zero then number is even or else it is odd
    if(a%2==0)
    {
        System.out.println("a is even");
    }
    else
    {
        System.out.println("a is odd");
    }

}

}